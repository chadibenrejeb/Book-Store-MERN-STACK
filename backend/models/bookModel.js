import mongoose from 'mongoose';

const bookSchema = new mongoose.Schema({
    id: {
        type: Number,
        required: true,
        unique: true,
    },
    title: {
        type: String,
        required: true,
    },
    author: {
        type: String,
        required: true,
    },
    publishYear: {
        type: Number,
        required: true,
    },
});

const Book = mongoose.model('Book', bookSchema);

export default Book;